#include<stdio.h>

float add(float num1,float num2)
{
return num1+num2;
}
float sub(float num1,float num2)
{
return num1-num2;
}
float product(float num1,float num2)
{
return num1*num2;
}
float div(float num1,float num2)
{
return num1 / num2;
}
float sqr(float num1)
{
return num1*num1;
}
float k_m(float kb)
{
return kb/1024;
}
float m_k(float mb)
{
return mb*1024;
}
float m_g(float mb)
{
return mb/1024;
}
float g_m(float gb)
{
return gb*1024;
}
float r_d(float rupe)
{
return rupe/75.3235;
}
float d_r(float dolr)
{
return dolr*75.3235;
}
float r_e(float rupe)
{
return rupe/85.7558;
}
float e_r(float euro)
{
return euro*85.7558;
}
float cm_m(float cm)
{
return cm/100;
}
float m_cm(float m)
{
return m*100;
}
float m_km(float m)
{
return m/1000;
}
float km_m(float km)
{
return km*1000;
}
